a, b = 9,5

hap, sub, gob, nmg = 0,0,0,0
mok = 0.0

hap = a+b
sub = a-b
gob = a*b
mok = a/b
nmg = a%b

# print('%d정수 %s문자열 %f실수 %c문자' %(데이터))
# print('%d*%d=%d' %(a,b,ret))
print( '%d+%d=%d' %(a,b,hap) )
print( '%d-%d=%d' %(a,b,sub) )
print( '%d*%d=%d' %(a,b,gob) )
print( '%d/%d=%d' %(a,b,mok) )
print( '%d %% %d=%d' %(a,b,nmg) )
print()
# print('{} * {} = {}'.format(a,b,ret) )
# print('{} * {} = {}'.format(a,b,ret))
print('{} + {} = {}' .format(a,b,hap))
print('{} - {} = {}' .format(a,b,sub))
print('{} * {} = {}' .format(a,b,gob))
print('{} / {} = {}' .format(a,b,mok))
print('{} % {} = {}' .format(a,b,nmg))
print()
# print( f'{변수 및 값}' )
# print(f'{a} * {b} = {ret}')
print(f'{a} + {b} = {hap}')
print(f'{a} - {b} = {sub}')
print(f'{a} * {b} = {gob}')
print(f'{a} / {b} = {mok}')
print(f'{a} % {b} = {nmg}')
print()
# print(변수, '문자', ~~ ) -> 나열식
# print(a, '*', b, '=', ret)
print(a, '+', b, '=', hap)
print(a, '-', b, '=', sub)
print(a, '*', b, '=', gob)
print(a, '/', b, '=', mok)
print(a, '%', b, '=', nmg)
print()

# print() / % / {} / f{} / a, -> 다양한 출력
